//ïŋ―Æšïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ã·Ļ
void koch(double x0, double y0, double x1, double y1, int k)
{
	//ïŋ―Æšïŋ―ïŋ―ïŋ―ïŋ―ßĩïŋ―5ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―
	double x2, y2, x3, y3, x4, y4;
	x2 = 2.0 / 3 * x0 + 1.0 / 3 * x1;
	y2 = 2.0 / 3 * y0 + 1.0 / 3 * y1;
	x3 = 1.0 / 3 * x0 + 2.0 / 3 * x1;
	y3 = 1.0 / 3 * y0 + 2.0 / 3 * y1;
	x4 = 1.0 / 2 * (x0 + x1) + sqrt(3.0) / 6 * (y1 - y0);	//ïŋ―ïŋ―Ņ§ïŋ―ïŋ―Ę―ïŋ―Æĩïŋ―
	y4 = 1.0 / 2 * (y0 + y1) - sqrt(3.0) / 6 * (x1 - x0);	//ïŋ―ïŋ―Ņ§ïŋ―ïŋ―Ę―ïŋ―Æĩïŋ―

	//ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―1ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ÐĩÝđïŋ―ïŋ―ïŋ―ïŋ―
	if (k > 1)
	{
		koch(x0, y0, x2, y2, k - 1);
		koch(x2, y2, x4, y4, k - 1);
		koch(x4, y4, x3, y3, k - 1);
		koch(x3, y3, x1, y1, k - 1);
	}
	//ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ÎŠŌŧïŋ―ïŋ―Öąïŋ―Óŧïŋ―ïŋ―ÆŋÆšïŋ―ïŋ―ïŋ―ïŋ―ïŋ―
	else
	{
		//ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―Æĩïŋ―Ãŋïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ßĩïŋ―ïŋ―ïŋ―ÉŦïŋ―ïŋ―ïŋ―ïŋ―ÎŠïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ÉŦ
		setlinecolor(RGB(rand() % 255, rand() % 255, rand() % 255));
		line(x0, y0, x2, y2);
		line(x2, y2, x4, y4);
		line(x4, y4, x3, y3);
		line(x3, y3, x1, y1);
	}
}

//ïŋ―ïŋ―ïŋ―Æšïŋ―ïŋ―ïŋ―ïŋ―ß·ïŋ―Ũ°ÎŠïŋ―Æšïŋ―ŅĐïŋ―ïŋ―
void kochsnow(int n)
{
	koch(50, 120, 450, 120, n);
	koch(450, 120, 250, 120 + 200 * sqrt(3.0), n);
	koch(250, 120 + 200 * sqrt(3.0), 50, 120, n);
}

